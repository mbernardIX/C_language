#include "libft.h"

char	*ft_strcat(char *s, const char *q)
{
	size_t i;

	i = 0;
	while (s[i])
		i++;
	while (*q)
	{
		s[i] = *q;
		i++;
		q++;
	}
	s[i] = '\0';
	return (&s[0]);
}
