#include "libft.h"

int	ft_is_prime(int nb)
{
	int mul;

	if (nb <= 1)
		return (0);
	mul = 2;
	while (mul * mul <= nb)
	{
		if (nb % mul == 0)
			return (0);
		mul++;
	}
	return (1);
}
