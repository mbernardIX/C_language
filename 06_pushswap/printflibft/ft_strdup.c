#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *src)
{
	int		len;
	char	*dst;
	int		i;

	if (src == NULL)
		return (NULL);
	len = 0;
	while (src[len])
		len++;
	dst = malloc(sizeof(char) * len + 1);
	if (dst == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
