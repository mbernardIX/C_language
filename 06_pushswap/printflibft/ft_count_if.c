#include "libft.h"

int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int sum;

	i = 0;
	sum = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			sum++;
		i++;
	}
	return (sum);
}
