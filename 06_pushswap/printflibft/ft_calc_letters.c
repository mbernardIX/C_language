#include "split.h"

int		ft_calc_letters(char const *s, char c)
{
	int i;

	while (*s)
	{
		i = 0;
		if (*s != c && *s)
		{
			while (*s != c && *s)
			{
				s++;
				i++;
			}
			return (i);
		}
		if (*s)
			s++;
	}
	return (0);
}
