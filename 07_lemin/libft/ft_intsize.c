#include "libft.h"

int			ft_intsize(int n)
{
	int	i;
	int	flag;

	flag = 0;
	i = 0;
	if (n == 0)
		i++;
	if (n < 0)
	{
		flag = 1;
		n = -n;
	}
	while (0 < n)
	{
		n = n / 10;
		i++;
	}
	return (i + flag);
}
