#include "libft.h"

void		ft_lstadd(t_list **alst, t_list *new)
{
	if (!alst || !new)
		return ;
	if (alst == NULL)
		new->next = NULL;
	if (alst != NULL)
		new->next = (*alst);
	(*alst) = new;
}
