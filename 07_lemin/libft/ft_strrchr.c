#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*p;

	p = s;
	while (*p != '\0')
	{
		p++;
	}
	while (p >= s)
	{
		if (*p == (unsigned char)c)
			return ((char*)p);
		p--;
	}
	return (NULL);
}
