#include "libft.h"

int		ft_sqrt(int nb)
{
	long i;

	i = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (nb >= i)
	{
		if (i * i == nb)
		{
			return (i);
		}
		i++;
	}
	return (i);
}
