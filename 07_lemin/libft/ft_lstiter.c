#include "libft.h"

void			ft_lstiter(t_list *lst, void (*f)(t_list*elem))
{
	t_list *save;

	if (!lst || !f)
		return ;
	while (lst->next != NULL)
	{
		save = lst->next;
		f(lst);
		lst = save;
	}
	f(lst);
}
